
public class Address {
	
	public String city; 
	public String country; 
	public String street; 
	public String postCode; 
	public String coords;  
	
	public Address (String city, String country, String street, String postCode, String coords) { 
		
		this.city = city; 
		this.country = country; 
		this.street = street; 
		this.postCode = postCode; 
		this.coords = coords;
		
	}

}
